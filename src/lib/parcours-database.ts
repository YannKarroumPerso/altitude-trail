import { Parcours } from "@/types";
import { estimateDurationHours } from "./parcours-utils";

// Dataset curé : 15 parcours trail / randonnée français emblématiques avec
// données d'itinéraire approximatives (5-8 waypoints par parcours, issus de
// passages reconnus — cols, refuges, villages — plutôt que d'une survey GPS).
// À usage éditorial et découverte, PAS pour navigation.
// Enrichissement futur : scripts/import-parcours.mjs (import GPX/Overpass).

function build(
  p: Omit<Parcours, "id" | "durationHours"> & { durationHours?: number }
): Parcours {
  return {
    ...p,
    id: p.slug,
    durationHours: p.durationHours ?? estimateDurationHours(p.distance, p.elevationGain),
  };
}

