function escapeRegex(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function findSeedBlockBounds(dataTs, slug) {
  // Les seed articles vivent après le marqueur "...generatedArticles,".
  const marker = "...generatedArticles,";
  const markerIdx = dataTs.indexOf(marker);
  if (markerIdx === -1) throw new Error("marqueur generatedArticles introuvable");
  const searchFrom = markerIdx + marker.length;
  const slugPattern = new RegExp(`\\{\\s*slug:\\s*"${escapeRegex(slug)}"[\\s\\S]*?\\n\\s{2}\\},`, "g");
  slugPattern.lastIndex = searchFrom;
  const match = slugPattern.exec(dataTs);
  if (!match) return null;
  return { start: match.index, end: match.index + match[0].length, block: match[0] };
}

function replaceInBlock(block, regex, replacement) {
  return block.replace(regex, replacement);
}

function applyFullUpdate(dataTs, update) {
  const bounds = findSeedBlockBounds(dataTs, update.slug);
  if (!bounds) {
    console.warn(`[fix] ${update.slug}: bloc introuvable`);
    return dataTs;
  }
  let block = bounds.block;

  // Replace title (la ligne title: "...")
  block = replaceInBlock(
    block,
    /(title:\s*)"[^"\\]*(?:\\.[^"\\]*)*"/,
    `$1${JSON.stringify(update.newTitle)}`,
  );

  // Replace excerpt
  block = replaceInBlock(
    block,
    /(excerpt:\s*)"[^"\\]*(?:\\.[^"\\]*)*"/,
    `$1${JSON.stringify(update.newExcerpt)}`,
  );

  // Replace date
  block = replaceInBlock(
    block,
    /(date:\s*)"[^"]*"/,
    `$1${JSON.stringify(update.newDate)}`,
  );

  // Replace tags line
  block = replaceInBlock(
    block,
    /tags:\s*\[[^\]]*\]/,
    update.newTagsLine,
  );

  // Remove content field entirely (the line will be regenerated by
  // generate-seed-content.mjs)
  block = block.replace(
    /\n\s*content:\s*"[^"\\]*(?:\\.[^"\\]*)*",?/,
    "",
  );

  return dataTs.slice(0, bounds.start) + block + dataTs.slice(bounds.end);
}

function applyDateOnlyUpdate(dataTs, update) {
  const bounds = findSeedBlockBounds(dataTs, update.slug);
  if (!bounds) {
    console.warn(`[fix] ${update.slug}: bloc introuvable`);
    return dataTs;
  }
  let block = bounds.block;
  block = replaceInBlock(
    block,
    /(date:\s*)"[^"]*"/,
    `$1${JSON.stringify(update.newDate)}`,
  );
  return dataTs.slice(0, bounds.start) + block + dataTs.slice(bounds.end);
}

async function main() {
  let dataTs = await fs.readFile(DATA_PATH, "utf8");
  const before = dataTs;

  for (const update of TITLE_UPDATES) {
    dataTs = applyFullUpdate(dataTs, update);
    console.log(`[fix] ${update.slug}: title/excerpt/date/tags mis à jour, content vidé`);
  }

  for (const update of DATE_ONLY_UPDATES) {
    dataTs = applyDateOnlyUpdate(dataTs, update);
    console.log(`[fix] ${update.slug}: date → ${update.newDate}`);
  }

  if (dataTs === before) {
    console.log("[fix] rien à modifier");
    return;
  }

  await fs.writeFile(DATA_PATH, dataTs, "utf8");
  console.log("[fix] data.ts mis à jour. Relance maintenant `node --env-file-if-exists=.env.local scripts/generate-seed-content.mjs` pour régénérer le content des 4 articles.");
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
